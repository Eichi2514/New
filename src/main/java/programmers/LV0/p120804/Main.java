package programmers.LV0.p120804;

class Solution {
    public int solution(int num1, int num2) {
        return num1 * num2;
    }
}

public class Main {
    public static void main(String[] args) {
        System.out.println(new Solution().solution(3, 4));
        System.out.println(new Solution().solution(27, 19));
    }
}